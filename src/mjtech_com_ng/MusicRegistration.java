    public void connect() {
        try {
            conn = DriverManager.getConnection(DATABASE_NAME, USERNAME, PASSWORD);
            st = conn.createStatement();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Could not connect to database! \n Error: " + ex, "Status", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public boolean musicExist(){
        boolean status = false;
        try {
           String Query = "SELECT * FROM musics where title='" + title.getText() +"' and year='" +year.getText()+"'";
            connect();
            st.execute(Query);
            ResultSet rs = st.getResultSet();
            if(rs.next())
                status = true;
            else {
                status = false;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.toString());  
        }
        return status; 
    }

