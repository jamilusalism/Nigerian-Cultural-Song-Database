    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String errorMsg = "";
        if ("".equals(title.getText())) {
            errorMsg += " Title is required \n";
        }
        if ("".equals(artist_name.getText())) {
            errorMsg += " Artist name is required \n";
        }
        if ("".equals(producer.getText())) {
            errorMsg += " Producer is required \n";
        }
        if ("".equals(genre.getText())) {
            errorMsg += " Genre is required \n";
        }
        if ("".equals(album.getText())) {
            errorMsg += " Album is required \n";
        }
        if ("".equals(year.getText())) {
            errorMsg += " Year is required \n";
        }
        
        if("".equals(errorMsg)){
            if(musicExist()){
                JOptionPane.showMessageDialog(this, "A user exit with either username, phone or email!", "Status", JOptionPane.ERROR_MESSAGE);
                return ;
            }
            try {
                connect();
                String sql = "INSERT INTO musics (title,artist_name,nickname,producer,genre,album,year)VALUES('"
                + title.getText()+ "'," + "'" + artist_name.getText()  + "'," + "'" + nickname.getText().toLowerCase()+ "'," + "'" 
                        + producer.getText() + "'," + "'" + genre.getText() + "'," + "'" + album.getText() + "'," + "'" 
                        + year.getText()+ "')" ;
                st.execute(sql);
               JOptionPane.showMessageDialog(this, "Music successful saved, click on Reset to add new music.", "Status", JOptionPane.INFORMATION_MESSAGE);    
               btnRegister.setEnabled(false);
            }
            catch(HeadlessException | SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.toString());
            }
        } else {
            JOptionPane.showMessageDialog(this, errorMsg,"Status",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

